package com.mediastrip.components;

/**
 * The Class Tag.
 */
public class Tag {
    
    /** The id. */
    private int    id;
    
    /** The wording. */
    private String wording;
	
	/**
	 * Gets the id.
	 *
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	
	/**
	 * Sets the id.
	 *
	 * @param id the new id
	 */
	public void setId(int id) {
		this.id = id;
	}
	
	/**
	 * Gets the wording.
	 *
	 * @return the wording
	 */
	public String getWording() {
		return wording;
	}
	
	/**
	 * Sets the wording.
	 *
	 * @param wording the new wording
	 */
	public void setWording(String wording) {
		this.wording = wording;
	}
}
